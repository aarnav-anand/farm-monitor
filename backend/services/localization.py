def t(lang: str | None, key: str, **kwargs: Any) -> str:
    lang_n = normalize_lang(lang)
    table = STRINGS.get(lang_n, STRINGS[DEFAULT_LANG])
    base = table.get(key) or STRINGS[DEFAULT_LANG].get(key) or key
    if not kwargs:
        return base
    try:
        return base.format(**kwargs)
    except Exception:
        return base


