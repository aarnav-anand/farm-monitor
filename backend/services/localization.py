SUPPORTED_LANGS = {"en", "hi", "gu"}
DEFAULT_LANG = "en"


def normalize_lang(lang: str | None) -> str:
    if not isinstance(lang, str):
        return DEFAULT_LANG
    lang = lang.strip().lower()
    return lang if lang in SUPPORTED_LANGS else DEFAULT_LANG


